    public boolean validarEntrada() throws ParseException {
        boolean validar = false;
        if (jDateChooserInicial.getDate() == null || jDateChooserFinal.getDate() == null) {

            mensaje.setText("Ingrese El rango De Fechas");
        } else {
            validar = true;
            llamarFechas();
            mensaje.setText("");

        }

        return validar;
    }

    public void llamarFechas() throws ParseException {
        SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat formato2 = new SimpleDateFormat("yyyy-MM-dd");
        fechaInicial = formato.format(jDateChooserInicial.getDate());
        fechaFinal = formato2.format(jDateChooserFinal.getDate());

    }



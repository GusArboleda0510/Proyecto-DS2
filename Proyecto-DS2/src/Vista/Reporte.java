    private void reporteEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reporteEntradaActionPerformed
        try {
            mensaje.setText("");
            if (validarEntrada()) {

                cr = new ControlReporte(fechaInicial, fechaFinal);
                if (cr.contarRegistrosEntradas() > 0) {
                    cr.crearExcelEntradas();
                    new MostrarMensaje("Reporte creado", null, true);
                    dispose();
                } else {
                    mensaje.setText("No existen registros");
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(Reporte.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_reporteEntradaActionPerformed


    private void EscuchaBuscar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EscuchaBuscar
        try {
            jlMensaje.setVisible(false);
            jlCarrera.setVisible(true);
            jLabel12.setVisible(true);
            documento=Integer.parseInt(jtCodEstudiante.getText());
            cs = new ControlRegistroSalida();
            
            p = DAOFactory.getPersona().consultarID(documento);
            pr = DAOFactory.getProgramasDAO().consultarID(p.getPrograma().hashCode());
            List<RegistroEntradaSalida> entradaSalida = DAOFactory.getRegistroEntradaSalidaDAO().consultarTodos();
            
            cs.registro(p,documento);

            ArrayList inf = cs.consultarTodo(p,pr,entradaSalida);

            jLNombre.setText((String) inf.get(0));
            jlCargo.setText((String) inf.get(1));

            if(inf.get(2).equals("")){
                jlCarrera.setVisible(false);
                jLabel12.setVisible(false);
            }else{
                jlCarrera.setText((String) inf.get(2));
            }

            jlRegHoraEntrada.setText((String) inf.get(3));
            jlFechaEntrada.setText((String) inf.get(4));

            if (!inf.get(5).equals("")) {
                mensaje("El Usuario ingresado, ya tiene registro de salida");
                existeSalida = true;
            }
        } catch (NumberFormatException e) {
            mensaje("El codigo debe ser numerico");
        } catch (Exception ex) {
            mensaje(ex.getMessage());
        }
    }//GEN-LAST:event_EscuchaBuscar

    

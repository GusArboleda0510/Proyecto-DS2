    public void modificar (String hora) throws Exception{
        Date auxHora = null;
        RegistroEntradaSalida regSalida = DAOFactory.getRegistroEntradaSalidaDAO().consultarID(cs.getCodigo());
        SimpleDateFormat formato = new SimpleDateFormat("HH:mm:ss");
        auxHora = formato.parse(hora);
        regSalida.setHoraSalida(auxHora);
            
        EntityManagerHelper.beginTransaction();
        DAOFactory.getRegistroEntradaSalidaDAO().modificar(regSalida);
        EntityManagerHelper.commit();
        EntityManagerHelper.closeEntityManager();
           
    }
    
